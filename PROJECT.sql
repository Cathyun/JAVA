CREATE TABLE C_SELLER(
 SELLER NVARCHAR2(10) PRIMARY KEY ,
 S_NAME NVARCHAR2(20) NOT NULL,
 S_PRICE NUMBER(20) NOT NULL,
 S_RUN NUMBER(20) NOT NULL,
 S_DATE NUMBER(20) NOT NULL,
 S_OIL NVARCHAR2(20) NOT NULL,
 S_CC NUMBER(20) NOT NULL,
 S_COLOR NVARCHAR2(20) NOT NULL,
 S_NUMBER NVARCHAR2(20) NOT NULL,
 S_PHONE NVARCHAR2(20) NOT NULL
 );
 
CREATE TABLE C_MEMBER(
    ID  NVARCHAR2(20) PRIMARY KEY,
    PW  NUMBER(4) CHECK (LENGTH(PW) = 4) NOT NULL,
    NAME    NVARCHAR2(20) NOT NULL,
    AGE     NUMBER(3) NOT NULL,
    PHONE   NVARCHAR2(20) NOT NULL,
    ADR    NVARCHAR2(50) NOT NULL,
    E_MAIL  NVARCHAR2(50) NOT NULL,
    ACCOUNTNUMBER NVARCHAR2(20) NOT NULL
);

CREATE TABLE BANK(
    ID NVARCHAR2(20),
    ACCOUNTNUMBER NVARCHAR2(20)PRIMARY KEY,
